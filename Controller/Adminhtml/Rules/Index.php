namespace Bounteous\StoreLocations\Controller\Adminhtml\Rules;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Magento\Backend\Model\View\Result\Page;
use Magento\Framework\View\Result\PageFactory;

class Index extends Action
{

    /**
     * @var PageFactory
     */
    protected $resultPageFactory;

    /**
     * @param Context $context
     * @param PageFactory $resultPageFactory
     */
    public function __construct(
        Context $context,
        PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }

    /**
     * Index action
     *
     * @return Page
     */
    public function execute(): Page
    {
        /** @var Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Bounteous_StoreLocations::rules');
        $resultPage->addBreadcrumb(__('Bounteous'), __('Bounteous'));
        $resultPage->addBreadcrumb(__('Manage item'), __('Manage Rule'));
        $resultPage->getConfig()->getTitle()->prepend(__('Manage Rule'));

        return $resultPage;
    }
}
